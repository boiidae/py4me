# Dictionaries are like lists
# except that they use keys instead of numbers to look up values
# example 1: list
lst = list()
lst.append(21)
lst.append(183)
print(lst)
lst[0] = 23
print(lst)

# example 2: dictionary
ddd = dict()
ddd['age'] = 21
ddd['course'] = 182
print(ddd)
ddd['age'] = 23
print(ddd)
